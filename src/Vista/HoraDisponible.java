    private void jbtn_MostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_MostrarActionPerformed
        // TODO add your handling code here:
        
        String numrut,estado,hora,nombre = "",especialidad;
        int dia,mes,ano,idHorario;
        
        DefaultTableModel modelo = (DefaultTableModel) this.jtb_Disponible.getModel();
        
        ArrayList<Horario> lista = me.obtenerHorarios();
        ArrayList<Medico> listaMedicos = me.obtenerTodosLosMedicos();
        
        modelo.setRowCount(0);
        
        for (Horario horario : lista) {
            numrut = horario.getNumRut();
            estado = horario.getEstado();
            hora = horario.getHora();
            dia = horario.getDia();
            mes = horario.getMes();
            ano = horario.getAno();
            idHorario = horario.getIdHorario();
            
            if ("1".equals(estado)) {
                estado = "Disponible"; 
            }else{
                estado = "Reservado";
            }
            Medico medico = null;
            for (Medico med : listaMedicos) {
                if (med.getNumRut().equals(numrut)) {
                    medico = med;
                    break;
                }
            }
            
            nombre = medico.getNombre();
            especialidad = medico.getDescEspecialidad();
            
            if (jcbx_especialidad.getSelectedItem().equals(especialidad)) {
                modelo.addRow(new Object [] {idHorario,dia+"/"+mes+"/"+ano,hora,numrut,nombre,especialidad,estado});
            }
        } 
    }//GEN-LAST:event_jbtn_MostrarActionPerformed

