    private void jbtn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_eliminarActionPerformed
        // TODO add your handling code here:
        
        int id;
        
        id = Integer.parseInt(this.jtxt_idEspecialidad.getText());
        
        Metodos metodos = new Metodos();
        
        int opcion = JOptionPane.showConfirmDialog(this, "¿Seguro desea Eliminar?", "Eliminar",0);
        
        if (opcion == 0) {
            metodos.eliminarEspecialidad(id);
            JOptionPane.showMessageDialog(null, "Especialidad Eliminada","Eliminar",1);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Acción Eliminar Especialidad Cancelada", "Eliminar",2);
        }
        
        
    }//GEN-LAST:event_jbtn_eliminarActionPerformed

