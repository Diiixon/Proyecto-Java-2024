    private void jbtn_mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_mostrarActionPerformed
        // TODO add your handling code here:
        
        int dia, mes, ano,telefono;
        String rut, nombre, correo, fechaNac;
        
        Metodos metodos = new Metodos();
        DefaultTableModel modelo = (DefaultTableModel) this.jtbl_usuarios.getModel();
        
        modelo.setRowCount(0);
        
        ArrayList<Usuario> lista = metodos.mostrarUsuarios();
        for (Usuario usuario : lista) {
            dia = usuario.getDia();
            mes = usuario.getMes();
            ano = usuario.getAno();
            fechaNac = dia+"-"+mes+"-"+ano;
            rut = usuario.getNumRut();
            nombre = usuario.getNombre();
            correo = usuario.getCorreo();
            telefono = usuario.getNumTelefono();
            
            modelo.addRow(new Object [] {rut,nombre,correo,telefono,fechaNac});
        }
        
    }//GEN-LAST:event_jbtn_mostrarActionPerformed

